     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy(Comment $comment)
    {
        try {
            $comment->delete();

            return redirect()->back();
        } catch (\Exception $e) {
            return to_route('items.index')->with([
